CREATE DEFINER=`love-media`@`%` PROCEDURE `sp_update_sales_adjust_days`(IN p_cycle_id INT)
proc: BEGIN
  DECLARE v_pickup DATE;
  DECLARE v_plant  DATE;
  DECLARE v_pred_days DECIMAL(8,3);
  DECLARE v_expected DATE;

  /* 1) 最初の実集荷日 */
  SELECT MIN(pickup_date) INTO v_pickup
  FROM collections
  WHERE cycle_id = p_cycle_id;

  IF v_pickup IS NULL THEN
    LEAVE proc; -- 集荷が無ければ何もしないで終了
  END IF;

  /* 2) 定植日 */
  SELECT plant_date INTO v_plant
  FROM cycles
  WHERE id = p_cycle_id;

  /* 3) 初回集荷"直前"の最新予測（無ければ最新） */
  SELECT p.pred_days
    INTO v_pred_days
  FROM predictions p
  WHERE p.cycle_id = p_cycle_id
    AND p.created_at <= v_pickup
  ORDER BY p.created_at DESC
  LIMIT 1;

  IF v_pred_days IS NULL THEN
    SELECT p.pred_days
      INTO v_pred_days
    FROM predictions p
    WHERE p.cycle_id = p_cycle_id
    ORDER BY p.created_at DESC
    LIMIT 1;
  END IF;

  IF v_pred_days IS NULL THEN
    LEAVE proc; -- そもそも予測が無い
  END IF;

  /* 4) 期待収穫日を“オンザフライ”で算出（整数日に丸め） */
  SET v_expected = DATE_ADD(v_plant, INTERVAL CAST(ROUND(v_pred_days) AS SIGNED) DAY);

  /* 5) 列がある環境のみ有効化（無ければコメントアウトのまま） */
